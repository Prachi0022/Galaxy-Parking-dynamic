          <script>
          	// Service Box Read More / Read Less Toggle
          	function toggleReadMore(btn) {
          		const content = btn.parentElement;
          		const textArea = content.querySelector('.text-area');
          		const text = textArea ? textArea.querySelector('.service-box-text') : content.querySelector(
          			'.service-box-text');

          		if (text.classList.contains('expanded')) {
          			text.classList.remove('expanded');
          			btn.textContent = 'Read More';
          		} else {
          			text.classList.add('expanded');
          			btn.textContent = 'Read Less';
          		}
          	}

          	// Hide/Show Read More buttons based on content height
          	function initReadMoreButtons() {
          		document.querySelectorAll('.service-box-items').forEach(function (card) {
          			const textArea = card.querySelector('.text-area');
          			const text = textArea ? textArea.querySelector('.service-box-text') : card.querySelector(
          				'.service-box-text');
          			const btn = card.querySelector('.read-toggle');

          			if (text && btn) {
          				// Reset styles to measure natural height
          				text.classList.remove('expanded');

          				// Get the computed style for line height
          				const computedStyle = window.getComputedStyle(text);
          				const lineHeight = parseFloat(computedStyle.lineHeight) || 20;
          				const threeLinesHeight = lineHeight * 3;

          				// Measure the actual height of the text
          				const textHeight = text.scrollHeight;

          				// Only show Read More button if text exceeds 3 lines
          				if (textHeight > threeLinesHeight) {
          					btn.style.display = 'inline-block';
          				} else {
          					btn.style.display = 'none';
          				}
          			}
          		});
          	}

          	// Initialize on DOM ready
          	document.addEventListener("DOMContentLoaded", function () {
          		// Run after a short delay to ensure styles are applied
          		setTimeout(initReadMoreButtons, 100);

          		// Re-check on window resize
          		window.addEventListener('resize', initReadMoreButtons);
          	});

          	// GSAP Timeline Animation - Fixed for Desktop
          	document.addEventListener("DOMContentLoaded", function () {
          		// Wait for GSAP and ScrollTrigger to be ready
          		if (typeof gsap !== 'undefined' && typeof ScrollTrigger !== 'undefined') {

          			gsap.registerPlugin(ScrollTrigger);

          			// Get all timeline items
          			const timelineItems = document.querySelectorAll('.timeline-item');
          			const timeline = document.querySelector('.timeline');
          			const line = document.querySelector('.timeline-line-progress');

          			if (timelineItems.length > 0) {
          				// Set initial state - ensure items are hidden for animation
          				gsap.set(timelineItems, {
          					opacity: 0,
          					y: 60
          				});

          				// Animate each timeline item
          				timelineItems.forEach((item, i) => {
          					// Animation direction: odd cards slide from left, even from right
          					let xDirection = (i % 2 === 0) ? -80 : 80;

          					gsap.to(item, {
          						opacity: 1,
          						y: 0,
          						x: 0,
          						duration: 0.8,
          						ease: "power3.out",
          						scrollTrigger: {
          							trigger: item,
          							start: "top 85%",
          							end: "top 50%",
          							toggleActions: "play none none none",
          							onEnter: function () {
          								// Ensure the item is visible when animation starts
          								gsap.set(item, {
          									opacity: 1,
          									y: 0
          								});
          							}
          						}
          					});
          				});

          				// Timeline progress line scrub animation
          				if (line && timelineItems.length) {
          					let lastItem = timelineItems[timelineItems.length - 1];

          					ScrollTrigger.create({
          						trigger: timeline,
          						start: "top 80px",
          						end: () => `bottom+=${lastItem.offsetHeight} top+=110`,
          						scrub: 0.75,
          						onUpdate: function (self) {
          							const total = timeline.offsetHeight - 20;
          							line.style.height = `${self.progress * total}px`;
          						}
          					});
          				}
          			}
          		} else {
          			// Fallback: Show items without animation if GSAP not available
          			console.log('GSAP not loaded - showing timeline items without animation');
          			document.querySelectorAll('.timeline-item').forEach(item => {
          				item.style.opacity = '1';
          			});
          		}
          	});

          </script>
